export interface FeatureOption {
  name: string
  desc: string
}

export interface Field {
  label: string
  type?: 'stars' | 'action' | 'select' | 'scale' | 'nps_scale' | 'feature_rating' | 'radio' | 'rating_na' | 'multi_select' | 'multi_dropdown' | 'screenshot_upload' | 'searchable_select' | 'pricing_scale' | 'feature_table' | 'integration_rating' | 'review_preview' | 'topic_chips' | 'date_input' | 'disclaimer' | 'star_review_combo' | 'ph_rating' | 'tag_textarea' | 'ai_questions' | 'ph_multi_rating'
  required?: boolean
  content?: string
  maxChars?: number | null
  options?: string[]
  scaleMax?: number
  scaleStart?: number
  scaleLabels?: Record<number, string>
  features?: FeatureOption[]
  hint?: string
  showWhen?: { field: number; value: string | string[] }
  hasSubRating?: boolean
  ratingQuestions?: { question: string; descriptions: string[] }[]
}

export interface AiHelper {
  description: string
  suggestions: string[]
}

export interface Tab {
  name: string
  title: string
  desc: string
  tip?: string
  fields: Field[]
  aiHelper?: AiHelper
}

export interface ChecklistItem {
  text: string
  ref: string
}

export interface Platform {
  id: string
  name: string
  productName: string
  color: string
  link: string
  desc: string
  tabs: Tab[]
  checklist: ChecklistItem[]
  fields: Field[]
}

function flattenFields(tabs: Tab[]): Field[] {
  return tabs.flatMap((t) => t.fields)
}

